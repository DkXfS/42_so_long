#include "ft_printf.h"

int	ft_nbrlen(int n, int precision)
{
	int		len;

	len = 0;
	if (n == 0 && precision != 0)
		len++;
	while (n != 0)
	{
		n /= 10;
		len++;
	}
	return (len);
}
